-- Завдання 1. Вставити у список через кожнi n елементiв вказане значення, напр. через n=2 значення ’z’: "1234590"⇒ "12z34z59z0".
insert :: Int -> a -> [a] -> [a]
insert 0 y xs = xs
insert n y [] = []
insert n y xs 
  | length xs < n = xs
  | otherwise = take n xs ++ [y] ++ insert n y (drop n xs)

