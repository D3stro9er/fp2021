-- Завдання 2. Знайти перше просте число в указаному дiапазонi.
isPrime :: Integral a => a -> Bool
isPrime k = (k > 1) && null [ x | x <- [2..k - 1], k `mod` x == 0]
 
func :: [Int]->Int 
func xs = if isPrime (head xs) then head xs else func (drop 1 xs)
